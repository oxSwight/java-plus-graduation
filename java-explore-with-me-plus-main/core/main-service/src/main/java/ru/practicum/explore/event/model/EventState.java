package ru.practicum.explore.event.model;

/**
 * Состояния событий в системе.
 */
public enum EventState {
    /**
     * Событие создано и ожидает модерации.
     * Может быть переведено в состояния PUBLISHED или CANCELED.
     */
    PENDING,

    /**
     * Событие опубликовано администратором.
     * Является финальным состоянием - опубликованное событие нельзя изменить.
     */
    PUBLISHED,

    /**
     * Событие отменено (инициатором или администратором).
     * Является финальным состоянием - отмененное событие нельзя изменить.
     */
    CANCELED
}